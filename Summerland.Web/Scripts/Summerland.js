/// Builds the HTML to mark the given date.
function BuildDateMarker(data) {
    //debugger;
    var classNames = GetDateClassNames(data.date);
    var toolTip = BuildToolTip(data.date, classNames);
    var marker = '<div class="dt ' + classNames + '" title="' + toolTip + '"><span>' + data.value + '</span></div>';

    return marker;
}

/// Gets the CSS class name to use for the given date.
function GetDateClassNames(date) {
    var classNames = calDates[+date];

    if (!classNames || classNames == "undefined")
        classNames = "dt-available";

    return classNames;
}

/// Builds a ToolTip from the given date and CSS class name.
function BuildToolTip(date, className) {

    //var toolTip = className.replace('dt-', '');

    var toolTip = 'Available';

    switch (className) {
        case 'dt-b':
            toolTip = 'booked';
            break;

        case 'dt-b-am':
            toolTip = 'booked AM';
            break;

        case 'dt-b-pm':
            toolTip = 'booked PM';
            break;

        case 'dt-p':
            toolTip = 'provisionally booked';
            break;

        case 'dt-p-am':
            toolTip = 'provisionally booked AM';
            break;

        case 'dt-p-pm':
            toolTip = 'provisionally booked PM';
            break;

        case 'dt-p-am-b-pm':
            toolTip = 'provisionally booked AM, booked PM';
            break;

        case 'dt-b-am-p-pm':
            toolTip = 'booked AM, provisionally booked PM';
            break;
    }

    toolTip = kendo.toString(date, 'dddd')
		+ ' ' + kendo.toString(date, 'D')
		+ ' - ' + toolTip;

    return toolTip;
}

$().ready(function () {
    // De-select calender selected date
    $(".k-state-selected").removeClass("k-state-selected");

    // Prevent calender buttons from being clicked
    var cancellables = $(".k-nav-fast,.k-other-month,.k-link,.k-header,.k-content,.k-calendar")
    cancellables.unbind();
    cancellables.click(function (e) {
        e.preventDefault();
        e.stopPropagation();
    });
});
